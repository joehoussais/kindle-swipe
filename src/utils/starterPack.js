/**
 * Generate highlight objects from starter quotes
 */
export function generateStarterHighlights() {
  return STARTER_QUOTES.map((quote, index) => ({
    id: `starter-${index}-${quote.text.slice(0, 20).replace(/\W/g, '')}`,
    text: quote.text,
    title: quote.title,
    author: quote.author,
    location: null,
    page: null,
    isStarter: true,
    category: quote.category,
    source: 'quote'
  }));
}

/**
 * Get quotes by category
 */
export function getQuotesByCategory(category) {
  return STARTER_QUOTES.filter(q => q.category === category);
}

/**
 * Get all available categories with counts
 */
export function getCategories() {
  const counts = {};
  STARTER_QUOTES.forEach(q => {
    counts[q.category] = (counts[q.category] || 0) + 1;
  });
  return Object.entries(counts).map(([category, count]) => ({ category, count }));
}

